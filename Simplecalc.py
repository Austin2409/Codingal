def add(x, y):
    return x + y
def subtract(x, y):
    return x - y
def multiply(x, y):
    return x * y
def divide(x, y):
    return x / y
num1 = int(input("Enter number 1"))
num2 = int(input("Enter number 2"))

print("Sum :", add(num1,num2))
print("Subtract :", subtract(num1,num2))
print("Multiply :", multiply(num1,num2))
print("Divide :", divide(num1,num2))